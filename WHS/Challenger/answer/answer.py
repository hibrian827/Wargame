for key in range(0x989681):  
  result = [0] * 32

  encoded = [
    0x4B, 0x13, 0x5D, 0x5F, 0x5F, 0x0F, 0x48, 0x7B,  
    0x7E, 0x68, 0x51, 0x74, 0x74, 0x68, 0x51, 0x6E,  
    0x2F, 0x23, 0x7A, 0x5F, 0x7F, 0x33, 0x3A, 0x6D,  
    0x6C, 0x6A, 0x3E, 0x6E, 0x61
  ]
  
  for i in range(29):
    result[i] = ((key >> (8 * (i & 3))) ^ encoded[i]) % 0x100
  result[29] = 0

  result = ''.join(list(map(chr, result)))
  if 'WHS_CTF' in result:
    print(key, result)